    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProducts.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một sản phẩm để sửa!");
            return;
        }

        // Lấy SKU từ bảng, sau đó tìm productId từ danh sách đã tải
        String skuCode = (String) tableModel.getValueAt(selectedRow, 0);
        Integer productIdToEdit = null;
        for (ProductDisplayDTO dto : this.productList) {
            if (dto.getSkuCode().equals(skuCode)) {
                productIdToEdit = dto.getProductId();
                break;
            }
        }

        if (productIdToEdit != null) {
            JDialogThemSuaSanPham dialog = new JDialogThemSuaSanPham((JFrame) SwingUtilities.getWindowAncestor(this), true, productIdToEdit);
            dialog.setVisible(true);
            loadFullData(); // Tải lại bảng sau khi sửa
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy thông tin sản phẩm!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProducts.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một sản phẩm để xoá!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá sản phẩm này không?", "Xác nhận", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            String skuCode = (String) tableModel.getValueAt(selectedRow, 0);
            Integer productIdToDelete = null;
            for (ProductDisplayDTO dto : this.productList) {
                if (dto.getSkuCode().equals(skuCode)) {
                    productIdToDelete = dto.getProductId();
                    break;
                }
            }

            try {
                if (productIdToDelete != null) {
                    productDAO.delete(productIdToDelete);
                    JOptionPane.showMessageDialog(this, "Xoá thành công!");
                    loadFullData(); // Tải lại bảng
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Xoá thất bại! Sản phẩm có thể đã được sử dụng trong hóa đơn.");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed


