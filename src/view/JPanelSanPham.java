    private void loadFullData() {
        try {
            this.productList = productDAO.selectAllForDisplay();
            fillTable(this.productList);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu sản phẩm!");
            e.printStackTrace();
        }
    }

    private void fillTable(List<ProductDisplayDTO> list) {
        tableModel.setRowCount(0); // Xóa dữ liệu cũ
        for (ProductDisplayDTO dto : list) {
            tableModel.addRow(new Object[]{
                dto.getSkuCode(),
                dto.getProductName(),
                dto.getColor(),
                dto.getSize(),
                dto.getCategoryName(),
                dto.getBrandName(),
                dto.getRetailPrice(),
                dto.getStockQuantity()
            });
        }
    }

    private void searchProducts() {
        String keyword = jTextField1.getText().trim().toLowerCase();
        if (keyword.isEmpty()) {
            fillTable(this.productList); // Hiển thị lại tất cả nếu ô tìm kiếm trống
            return;
        }

        List<ProductDisplayDTO> filteredList = this.productList.stream()
                .filter(p -> p.getProductName().toLowerCase().contains(keyword)
                || p.getSkuCode().toLowerCase().contains(keyword))
                .collect(Collectors.toList());

        fillTable(filteredList);
    }

